
# Move the mouse instantly
pyautogui.moveTo(100, 200)

# Move over 1 second
pyautogui.moveTo(500, 500, duration=1.5)

# Drag the mouse
pyautogui.dragTo(600, 600, duration=1)

# Click the mouse
pyautogui.click(100, 200)

# Double click
pyautogui.doubleClick()

# Right click
pyautogui.rightClick(150, 150)
